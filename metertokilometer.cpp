#include <iostream>
using namespace std;
int main()
{
    int meters , centimeters;
    cout<<"\t\t\tA simple Program to convert Meters to Centimeters";
    cout<<"\n\nEnter the value in meters = ";
    cin>>meters;
    centimeters=meters*100;
    cout<<"\n"<<"The Value of "<<meters<<" M in centimeter is "<<centimeters<<" CM";
    return 0;
}
